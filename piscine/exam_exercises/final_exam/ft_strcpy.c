#include <stdio.h>
#include <string.h>
#include <unistd.h>

char	*ft_strcpy(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s2[i] != '\0')
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}

int	main(void)
{
	char	str1[] = "xxx";
	char	str2[] = "prueba456";

	printf("%s", ft_strcpy(str1, str2));
}
