unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	contador;
	unsigned int	i;

	contador = 0;
	i = 0;
	while (src[contador] != '\0')
		contador++;
	if (size != 0)
	{
		while (src[i] != '\0' && i < (size - 1))
		{
			dest[i] = src[i];
			i++;
		}
		dest[i] = '\0';
	}
	return (contador);
}
